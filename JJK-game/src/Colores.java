/**
 * Constantes de color ANSI centralizadas.
 * Referencia: Colores.RESET, Colores.VERDE, etc.
 */
public final class Colores {
    private Colores() {}
    public static final String RESET        = "\u001B[0m";
    public static final String NEGRITA      = "\u001B[1m";
    public static final String ROJO         = "\u001B[31m";
    public static final String VERDE        = "\u001B[32m";
    public static final String AMARILLO     = "\u001B[33m";
    public static final String AZUL         = "\u001B[34m";
    public static final String MAGENTA      = "\u001B[35m";
    public static final String CYAN         = "\u001B[36m";
    public static final String BLANCO       = "\u001B[37m";
    public static final String ROJO_INT     = "\u001B[91m";
    public static final String VERDE_INT    = "\u001B[92m";
    public static final String AMARILLO_INT = "\u001B[93m";
    public static final String AZUL_INT     = "\u001B[94m";
    public static final String MAGENTA_INT  = "\u001B[95m";
    public static final String CYAN_INT     = "\u001B[96m";
}
